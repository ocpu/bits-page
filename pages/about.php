<h2>
Svenska
</h2>
<p>
Det är vi som är BITS –  en del av Blekinge studentkår.
Vårt uppdrag är att hålla på med utbildningsbevakning. Det vill säga
hålla ett öga på skolan, specifikt de program vi ansvarar för. Är det
något som inte går rätt och riktigt till går det att kontakta oss och vi
kommer försöka lösa problemet. Utöver det har vi även tid att arrangera
roliga evenemang.

Vi som sitter med i styrelsen gör det helt ideellt och det är fritt fram
för alla att engagera sig om ni vill.
</p>
<h2>
English
</h2>
<p>
Hi we are BITS - a part of Blekinge studentunion.
Our mission is to mainly surveying the different programs we are responsible for making sure they are fair,
useful and run smoothly. So if you experience anything contradicting this please contact us and we will do our
Best to forward and solve it in an efficient manner. Beyond that we also do several fun events through the year.
You can find info about them here on the site and you can participate through our Facebook page.
